int	ft_isalpha_on_ptr(char *ptr)
{
    int i;
    

    i = 0;
    while(ptr[i])
    {
       	if ((ptr[i] >= 65 && ptr[i] <= 90) || (ptr[i] >= 97 && ptr[i] <= 122))
		    return (1); 
        i++;
    }
	return (0);
}
